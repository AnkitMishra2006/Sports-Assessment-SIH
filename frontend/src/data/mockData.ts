export const calculateOverallScore = (testScores: Record<string, any>): number => {
  const weights = {
    "vertical-jump": 0.15,
    "sit-ups": 0.15,
    "shuttle-run": 0.15,
    "endurance-run": 0.2,
    "push-ups": 0.1,
    "flexibility": 0.1,
    "balance": 0.05,
    "broad-jump": 0.1
  };

  let totalScore = 0;
  let totalWeight = 0;

  Object.entries(testScores).forEach(([testId, data]) => {
    if (data.percentile && weights[testId as keyof typeof weights]) {
      totalScore += data.percentile * weights[testId as keyof typeof weights];
      totalWeight += weights[testId as keyof typeof weights];
    }
  });

  return totalWeight > 0 ? Math.round(totalScore / totalWeight) : 0;
};